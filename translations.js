  function normalizeLanguage(language) {
    return SUPPORTED_LANGUAGES.includes(language) ? language : "en";
  }

  function getLanguage() {
    try {
      return normalizeLanguage(localStorage.getItem(LANGUAGE_STORAGE_KEY) || "en");
    } catch {
      return "en";
    }
  }

  function setLanguage(language) {
    const normalizedLanguage = normalizeLanguage(language);
    try {
      localStorage.setItem(LANGUAGE_STORAGE_KEY, normalizedLanguage);
    } catch {}
    return normalizedLanguage;
  }

  function t(key, params = {}, language = getLanguage()) {
    const normalizedLanguage = normalizeLanguage(language);
    const value = dictionaries[normalizedLanguage]?.[key] ?? dictionaries.en[key] ?? key;

    return String(value).replace(/\{(\w+)\}/g, (_, token) => (
      Object.prototype.hasOwnProperty.call(params, token) ? params[token] : `{${token}}`
    ));
  }

  function applyTranslations(root = document) {
    const language = getLanguage();
    document.documentElement.lang = language;

    root.querySelectorAll("[data-i18n]").forEach((element) => {
      element.textContent = t(element.getAttribute("data-i18n"), {}, language);
    });

    root.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
      element.setAttribute("placeholder", t(element.getAttribute("data-i18n-placeholder"), {}, language));
    });

    root.querySelectorAll("[data-i18n-title]").forEach((element) => {
      element.setAttribute("title", t(element.getAttribute("data-i18n-title"), {}, language));
    });
  }

  window.AppI18n = {
    LANGUAGE_STORAGE_KEY,
    SUPPORTED_LANGUAGES,
    dictionaries,
    getLanguage,
    setLanguage,
    t,
    applyTranslations
  };
